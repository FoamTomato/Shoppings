package com.sybinal.shop.model;

public class codeResult {
	private String code;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	@Override
	public String toString() {
		return "codeResult [code=" + code + "]";
	}

	public codeResult(String code) {
		super();
		this.code = code;
	}

}
